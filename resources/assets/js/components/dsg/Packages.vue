          >
            <v-text-field
              v-model="item.date_processed"
              readonly
              label="Date Processed"
              prepend-icon="event_note"
            />
          </v-flex>
          <v-flex 
            sm12
            md2
          >
            <v-autocomplete
              :items="bins"
              v-model="item.bin_id"
              item-text="code"
              item-value="id"
              required
              label="Choose Bin"
              light
              prepend-icon="view_comfy"
            />
          </v-flex>
          <v-flex 
            sm12
            md2
          >
            <v-autocomplete
              :items="rates"
              v-model="item.handling_type"
              item-text="name"
              item-value="id"
              required
              label="Choose Handling Note"
              light
              prepend-icon="receipt"
            />
          </v-flex>
          <v-flex 
            sm12
            md2
          >
            <v-autocomplete
              :items="store_at"
              v-model="item.store_at"
              required
              label="Store At"
              light
              prepend-icon="dns"
            />
          </v-flex>
          <v-flex 
            sm12
            md2
          >
            <v-text-field
              v-model="item.style_no"
              label="Style No."
              prepend-icon="style"
            />
          </v-flex>
          <v-flex 
            sm12
