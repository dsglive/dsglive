        >
          <!-- 6flex tab-->

          <v-flex 
            sm12
            md2
          >
            <v-text-field
              v-model="item.po_no"
              readonly
              label="PO No."
              prepend-icon="bookmark"
            />
          </v-flex>
          <v-flex
            sm12
            md2
          >
            <v-text-field
              v-model="item.date_received"
              readonly
              label="Date Received"
              prepend-icon="event_available"
            />
          </v-flex>
          <v-flex 
            sm12
